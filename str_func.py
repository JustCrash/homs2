def with_a_capital_letter(text):
    """
    принимает текст и выводите 
    его капсом
    """
    print(text.upper())


with_a_capital_letter("блин-блинский")


def get_title(text):
    """
    получает текст и выводит каждое слово
    с заглавной буквы
    """
    print(text.title())


get_title("ёлки-иголки")